#Написати програму введення двох слів (через пропуск в один рядок). Визначити булеви значення для оператора in перевірки входження першого слова до другого. А також для операторів ==, >, <. Всі булеві значення об'єднати в один рядок через пропуск і вивести на екран.

a, b = input().split()
print(a in b, a == b, a > b, a < b)

#З клавіатури вводяться дві літери (в один рядок через пропуск). Вивести на екран наступний рядок:
#"Коди: <літера1> = <код букви1>, <літера2> = <код букви2>"

a, b = input().split()
print(f"Код: {a} = {ord(a)}, {b} = {ord(b)}")