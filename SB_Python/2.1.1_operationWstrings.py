#Напишіть програму введення двох слів через проміжок. Сформуйте нову строку, дублюючи перше слово два рази, а друге - три рази (всі слова в результуючій строці повинні ідти через пробіл). Результат виведіть на екран.
#Програму слід реалізувати без використання F-строк, а із застосуванням оператора дублювання рядка.

a, b = map(str, input().split())
print((a + " ") * 2 + (b + " ") * 3)

#Виконується розрахунок двох цілочисленних значень у змінних a і b (вводяться в одну строчку через пробіл). Необхідно сформувати строку типу: "Змінна a = <значення>, змінна b = <значення>", використовуючи оператор конкатенації (з'єднання) строки. Результат виведіть на екран.
#PS F-строки в програмі не використовувати.

a, b = input().split()
print("Змінна a = " + a + ", Змінна b = " + b)

#Написати програму введення рядка та формування нового рядка виду: "Рядок: <введений рядок>. Довжина: <довжина рядка>". Результат сформованого рядка вивести на екран.
#PS У програмі F-рядки не використовувати.

a = input()
